                <div class="col-lg-8 col-md-8 col-sm-7">
                    <p>PPDA is mandated by the PPD Act of 2017 to regulate, monitor and oversee public procurements in Malawi.
                        The Act requires that PPDA organizes and maintain a centralized system in form of a database or list to
                        provide centralized information on potential suppliers of goods, works or services. In this regard, PPDA set up
                        a supplier registration process to accomplish this requirement. In essence, businesses are required by law to register
                        with the authority if they are to conduct any business proceedings with the Governments Ministries, Departments and Agencies.
                        <br>
                    </p>

